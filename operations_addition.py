# same-type addition
print('same-type addition')
ii = 1 + 2  # <int> + <int>
print(ii, type(ii))
ff = 1.1 + 2.2  # <float> + <float>
print(ff, type(ff))
ss = 'saber' + ' fadaee'  # <str> + <str>
print(ss, type(ss))
ll = [1, 2] + [3, 5]  # <list> + <list>
print(ll, type(ll))

